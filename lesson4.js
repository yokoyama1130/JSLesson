// Boolean型について
// booleanはシンプルで、trueかfalseの2種類しかない
let isActiveGame = false;
console.log(isActiveGame);

isActiveGame = true;
console.log(isActiveGame);



// 変数のデータの型は変えられる！
// 使う場面は稀だが、変えられるってことを覚えておこう！
let numPuppies = 23; // Number
numPuppies = false; // Booleanに変身！
numPuppies = 100; // 再びNumberに変身!
