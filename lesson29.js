// オブジェクトはプロパティの集合体
// プロパティはキーと値のペア
// インデックスでデータにアクセスするのではなく、キーを使ってアクセスする


// オブジェクトリテラルを使う
const person = {firstName: "Taro", lastName: "Yamada"};
console.log(person);

const cat = {
    name: "Tama",
    age: 2,
    color: ["orange", "white"],
    isHugry: true
};
console.log(cat);
