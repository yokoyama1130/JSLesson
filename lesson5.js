// 変数の命名規則
let hoge = 1;
// 変数の間にスペースとかは入れられない。くっつける必要がある。
// 数字も入れられる
let user1 = 10;
// しかし、数字から始める変数名は禁止
// _アンダースコアから始めるのはありだけど、一般的ではない。
let _user = 1;
// ちなみに日本語でも良いけど一般的ではない。
let 日本語 = 100;

// キャメルケースで変数を命名するのが一般的
let currentYear = 1999;

// これはスネークケース
let current_year = 1999;

// 最初の文字も大文字にするのはパスカルケース
let CuurentYear = 1999;

// Booleanの変数名には最初にisをつけることが多い
let isLoggedInUser = true;
// hasを使うこともある。（あるのかないのかみたいなとき）
let hasHeader = false;


